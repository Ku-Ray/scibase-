export interface ConcernGuidePaperRef {
  /** 引用論文タイトル（英文） */
  title: string
  /** ジャーナル名 */
  journal: string
  year: number
  /** スタディタイプ（RCT・メタ解析等） */
  studyType: 'meta-analysis' | 'rct' | 'cohort' | 'observational' | 'review'
  sampleSize?: number
  durationWeeks?: number
  /** 1行で示す key finding（日本語） */
  keyFinding: string
  doi?: string
  pmid?: string
}

/** 章[4] シミの3タイプ・メカニズム */
export interface ConcernGuideMechanismType {
  /** 例: 「肝斑」 */
  typeName: string
  /** 1行サブタイトル */
  subtitle: string
  /** 見分け方の特徴（箇条書き） */
  signs: string[]
  /** メカニズム本文（300-400字） */
  body: string
  /** 引用論文 */
  paperRefs: ConcernGuidePaperRef[]
}

/** 章[5] タイプ別の解決策・本文 + 商品ブロック */
export interface ConcernGuideProductBlock {
  /** どの成分の商品か */
  ingredientSlug: string
  /** ingredient.products[index] を参照（省略時は rank=1 製品） */
  productIndex?: number
  /** なぜこの商品か（80-120字・公式訴求と論文に厳密対応） */
  whyJa: string
  /** ハイライト見出し（任意・「2%濃度・RCT条件」等） */
  badge?: string
}

export interface ConcernGuideSolutionType {
  /** 例: 「肝斑」 */
  typeName: string
  /** メインで推す成分 */
  bestPickSlug: string
  /** 副次的に組み合わせる成分（任意） */
  secondarySlugs?: string[]
  /** 本文（600-800字・1人称・つまり言い換え必須） */
  body: string
  /** 本文末に並べる商品ブロック */
  productBlocks: ConcernGuideProductBlock[]
}

/** 章[6] 失敗パターン */
export interface ConcernGuideFailurePattern {
  /** 「失敗1：8週続けない」 */
  title: string
  /** 200-300字 */
  body: string
}

/** 章[7] セルフチェック（IKEA効果） */
export interface ConcernGuideSelfCheckQuestion {
  /** 設問（特徴の言語化） */
  q: string
  /** 該当タイプ（mechanismByType[].typeName と一致） */
  typeAnswer: string
  /** そのタイプのおすすめ成分slug */
  recommendIngredientSlugs: string[]
}

