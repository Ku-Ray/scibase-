export interface ConcernGuidePaperRef {
  /** 引用論文タイトル（英文） */
  title: string
  /** ジャーナル名 */
  journal: string
  year: number
  /** スタディタイプ（RCT・メタ解析等） */
  studyType: 'meta-analysis' | 'rct' | 'cohort' | 'observational' | 'review'
  sampleSize?: number
  durationWeeks?: number
  /** 1行で示す key finding（日本語） */
  keyFinding: string
  doi?: string
  pmid?: string
}

/** 章[4] シミの3タイプ・メカニズム */
export interface ConcernGuideMechanismType {
  /** 例: 「肝斑」 */
  typeName: string
  /** 1行サブタイトル */
  subtitle: string
  /** 見分け方の特徴（箇条書き） */
  signs: string[]
  /** メカニズム本文（300-400字） */
  body: string
  /** 引用論文 */
  paperRefs: ConcernGuidePaperRef[]
}

/** 章[5] タイプ別の解決策・本文 + 商品ブロック */
export interface ConcernGuideProductBlock {
  /** どの成分の商品か */
  ingredientSlug: string
  /** ingredient.products[index] を参照（省略時は rank=1 製品） */
  productIndex?: number
  /** なぜこの商品か（80-120字・公式訴求と論文に厳密対応） */
  whyJa: string
  /** ハイライト見出し（任意・「2%濃度・RCT条件」等） */
  badge?: string
}

export interface ConcernGuideSolutionType {
  /** 例: 「肝斑」 */
  typeName: string
  /** メインで推す成分 */
  bestPickSlug: string
  /** 副次的に組み合わせる成分（任意） */
  secondarySlugs?: string[]
  /** 本文（600-800字・1人称・つまり言い換え必須） */
  body: string
  /** 本文末に並べる商品ブロック */
  productBlocks: ConcernGuideProductBlock[]
}

/** 章[6] 失敗パターン */
export interface ConcernGuideFailurePattern {
  /** 「失敗1：8週続けない」 */
  title: string
  /** 200-300字 */
  body: string
}

/** 章[7] セルフチェック（IKEA効果） */
export interface ConcernGuideSelfCheckQuestion {
  /** 設問（特徴の言語化） */
  q: string
  /** 該当タイプ（mechanismByType[].typeName と一致） */
  typeAnswer: string
  /** そのタイプのおすすめ成分slug */
  recommendIngredientSlugs: string[]
}

/** タイプ別マイルストーン（時間割引対抗の callout 用） */
export interface ConcernGuideMilestoneByType {
  /** mechanismByType[].typeName と一致 */
  typeName: string
  /** 例: { period: '3ヶ月', sign: '抜け毛量がやや減り始める' } */
  milestones: { period: string; sign: string }[]
}

/** タイプ別 高単価アフィCTA（Phase C で導入） */
export interface ConcernGuideClinicCTAByType {
  /** mechanismByType[].typeName と一致 */
  typeName: string
  /** 例:「もっと本気で止めたい人へ」等 */
  headline: string
  /** 80-150字・サプリとの棲み分けを明記 */
  body: string
  products: {
    name: string
    brand?: string
    /** ASPアフィURL */
    url: string
    /** 月額または1回あたりの相場 */
    priceJpy?: number
    /** なぜこのクリニック・80-120字 */
    whyJa: string
    /** 「初診¥1,500」等 */
    badge?: string
  }[]
}

export interface ConcernGuide {
  concernSlug: string
  /** h1兼タイトル */
  title: string
  /** 1行サマリー（30字目安） */
  summary: string
  publishedAt: string
  updatedAt: string
  /** 章[2] 著者の前置き（400-600字・1人称） */
  introNote: string
  /** 章[3] 結論先出し（3-4行） */
  bottomLine: string
  /** 章[4] 3タイプメカニズム */
  mechanismByType: ConcernGuideMechanismType[]
  /** 章[5] タイプ別の解決策 */
  solutionByType: ConcernGuideSolutionType[]
  /** 章[6] 失敗パターン */
  failurePatterns: ConcernGuideFailurePattern[]
  /** 章[7] セルフチェック */
  selfCheck: {
    intro: string
    questions: ConcernGuideSelfCheckQuestion[]
  }
  /** 章[8] FAQ（FAQPage JSON-LD と同期） */
  faqs: { q: string; a: string }[]
  /** 章[9] 締めくくりの行動ステップ（:::summary callout として描画） */
  closingSummary: string
  /** 章[10] 関連既存記事 slug（/articles/[slug]） */
  relatedArticleSlugs: string[]

  /** SEO用<title>タグ（H1とは別）。未指定時は title をフォールバック */
  seoTitle?: string
  /** Meta description（CTR最適化用個別化）。未指定時は summary をフォールバック */
  metaDescription?: string
  /** 価格アンカリング（bottomLine 直後に表示する1-2行） */
  priceAnchor?: string
  /** タイプ別 3-12ヶ月マイルストーン（solutionByType の各typeNameと一致） */
  milestonesByType?: ConcernGuideMilestoneByType[]
  /** 成功パターン（failurePatterns 後に gain framing として配置）3-5項目 */
  successProfile?: string[]
  /** タイプ別 高単価アフィCTA（typeName単位で配置・全TYPEに置かないこと） */
  clinicCTAByType?: ConcernGuideClinicCTAByType[]
}

